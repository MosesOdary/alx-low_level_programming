


long long add(long long a, long long b)
{
	return (a + b);
}

long long sub(long long a, long long b)
{
	return (a - b);
}

long long mul(long long a, long long b)
{
	return (a * b);
}

long long div(long long a, long long b)
{
	return (a / b);
}

long long mod(long long a, long long b)
{
	return (a % b);
}


